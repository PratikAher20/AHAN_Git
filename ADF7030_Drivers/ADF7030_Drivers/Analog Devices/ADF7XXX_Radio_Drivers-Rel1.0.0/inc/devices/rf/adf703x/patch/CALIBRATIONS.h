/**
 * @brief		The ADF7030-1 RX calibrations are implemented in FW. The ADC NOTCH and OCL offline calibrations
 * 			are included in the P3.2 ROM memory while the remaining ones are part of a single patch called “OffLineCals”.
 * 			A calibration can be executed both in response to a DO_CAL command (0x89) from the PHY_ON state only or,
 * 			alternatively, in response to the individual calibration commands.
 * 
 * @details		This file is for ADI Internal development only. 
 * 
 * HOST SEQUENCES:
 * --------------- 
 * 	
 * 	- Enabling the Calibration patch for the first time:
 * 		0. Radio PHY must be in PHY_OFF
 * 		1. Load the profiles, Generic Pkt, LUT's,
 * 		2. Load Freq Changes, Calibration Enable Bits etc.
 * 		3. Issue Cmd_Config_Dev.
 * 		4. Transfert the Calibration F/W
 * 		5. Enable CALIBRATIONS patch
 * 		6. Issue Phy On
 * 		7. Issue Do Cal
 * 		8. Readback and save calibration results
 * 		9. Disable CALIBRATIONS patch
 * 		
 * 	- Disabling CALIBRATIONS patch
 * 		1. Write (0x20000000 + <EjectSeq>) at location 0x20000130
 * 		
 * 	- Enabling CALIBRATIONS patch
 * 		1. Write (0x20000000 + <InjectSeq>) at location 0x20000130
 *  
 * 	@note ___The CALIBRATIONS f/w reside outside the BBRAM, user must reload the patch between sleep episodes___
 * 	@note ___Calibration results should save off cal results in the event of a system reset or power cycle___
 *  	
 */ 


